async function streamChatMessage(messages, settings, onChunk, wordContext) {
  const { llmServerUrl, llmApiKey, llmModel, llmTemperature, systemPrompt } = settings;
  const fullMessages = [
    {
      role: "system",
      content: wordContext ? `${systemPrompt}

The user just looked up the word: "${wordContext}". Use this as context for your response.` : systemPrompt
    },
    ...messages
  ];
  let apiUrl = llmServerUrl.replace(/\/+$/, "");
  const isOllama = apiUrl.includes("localhost:11434") || apiUrl.includes("127.0.0.1:11434");
  if (isOllama) {
    apiUrl += "/api/chat";
  } else {
    apiUrl += "/v1/chat/completions";
  }
  const headers = {
    "Content-Type": "application/json"
  };
  if (llmApiKey) {
    headers["Authorization"] = `Bearer ${llmApiKey}`;
  }
  const body = isOllama ? JSON.stringify({
    model: llmModel,
    messages: fullMessages,
    stream: true,
    options: {
      temperature: llmTemperature
    }
  }) : JSON.stringify({
    model: llmModel,
    messages: fullMessages,
    temperature: llmTemperature,
    stream: true
  });
  try {
    const response = await fetch(apiUrl, {
      method: "POST",
      headers,
      body
    });
    if (!response.ok) {
      const errorText = await response.text();
      return {
        message: "",
        error: `API error (${response.status}): ${errorText}`
      };
    }
    let fullMessage = "";
    if (isOllama) {
      const reader = response.body?.getReader();
      if (!reader) {
        return { message: "", error: "No response body" };
      }
      const decoder = new TextDecoder();
      let buffer = "";
      while (true) {
        const { done, value } = await reader.read();
        if (done) break;
        buffer += decoder.decode(value, { stream: true });
        const lines = buffer.split("\n");
        buffer = lines.pop() || "";
        for (const line of lines) {
          if (!line.trim()) continue;
          try {
            const json = JSON.parse(line);
            const content = json.message?.content || "";
            if (content) {
              fullMessage += content;
              onChunk(content);
            }
            if (json.done) break;
          } catch {
          }
        }
      }
    } else {
      const reader = response.body?.getReader();
      if (!reader) {
        return { message: "", error: "No response body" };
      }
      const decoder = new TextDecoder();
      let buffer = "";
      while (true) {
        const { done, value } = await reader.read();
        if (done) break;
        buffer += decoder.decode(value, { stream: true });
        const lines = buffer.split("\n");
        buffer = lines.pop() || "";
        for (const line of lines) {
          if (!line.startsWith("data: ")) continue;
          const data = line.slice(6).trim();
          if (data === "[DONE]") continue;
          try {
            const json = JSON.parse(data);
            const content = json.choices?.[0]?.delta?.content || "";
            if (content) {
              fullMessage += content;
              onChunk(content);
            }
          } catch {
          }
        }
      }
    }
    return { message: fullMessage };
  } catch (err) {
    return {
      message: "",
      error: `Connection error: ${err instanceof Error ? err.message : String(err)}. Make sure your LLM server is running at ${llmServerUrl}`
    };
  }
}

